def parse_obj(filename):
    vertices = []
    final_vbo_data = []
    full_path = os.path.join(TARGET_FOLDER, filename)
    
    try:
        with open(full_path, 'r') as f:
            lines = f.readlines()
    except FileNotFoundError:
        print(f"❌ Error: Still can't find {full_path}")
        return None

    for line in lines:
        if line.startswith('#'): continue
        parts = line.split()
        if not parts: continue

        if parts[0] == 'v':
            vertices.append([float(parts[1]), float(parts[2]), float(parts[3])])
        elif parts[0] == 'f':
            face_components = parts[1:]
            triangle_indices = [0, 1, 2] if len(face_components) == 3 else [0, 1, 2, 0, 2, 3]
            for i in triangle_indices:
                idx = int(face_components[i].split('/')[0]) - 1
                if idx < len(vertices):
                    final_vbo_data.extend(vertices[idx])
    return final_vbo_data

